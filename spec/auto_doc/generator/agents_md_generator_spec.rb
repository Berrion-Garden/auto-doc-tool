# frozen_string_literal: true

require "spec_helper"
require "tmpdir"

RSpec.describe AutoDoc::Generator::AgentsMdGenerator do
  subject(:generator) { described_class }

  let(:module_name) { "app" }
  let(:tree_text) do
    "app/\n  ├── models/\n  │   └── user.rb\n  └── controllers/\n      └── users_controller.rb\n"
  end
  let(:files) do
    [
      {
        name: "user.rb",
        path: "/project/app/models/user.rb",
        classes: [
          { name: "User", type: :class, line: 1, has_doc?: true },
          { name: "find", type: :method, line: 5, has_doc?: false }
        ],
        imports: [{ path: "ActiveRecord::Base", type: :require }]
      }
    ]
  end

  describe ".generate" do
    it "returns rendered markdown with the module name" do
      result = generator.generate(module_name, tree_text, files)
      expect(result).to include("# app")
    end

    it "includes the directory tree" do
      result = generator.generate(module_name, tree_text, files)
      expect(result).to include("user.rb")
      expect(result).to include("models/")
    end

    it "includes dependency table headers" do
      result = generator.generate(module_name, tree_text, files)
      expect(result).to include("Dependencies")
    end

    it "includes public API surface table" do
      result = generator.generate(module_name, tree_text, files)
      expect(result).to include("User")
      expect(result).to include("class")
    end

    it "includes the generated timestamp" do
      result = generator.generate(module_name, tree_text, files)
      expect(result).to match(/Generated by auto-doc/)
    end

    context "with empty files data" do
      let(:files) { [] }

      it "renders with no-data rows" do
        result = generator.generate(module_name, tree_text, files)
        expect(result).to include("No external dependencies")
        expect(result).to include("No public symbols")
      end
    end

    context "with output_path provided" do
      let(:output_dir) { Dir.mktmpdir }
      let(:output_path) { File.join(output_dir, "AGENTS.md") }

      after { FileUtils.remove_entry(output_dir) }

      it "writes the rendered content to file" do
        generator.generate(module_name, tree_text, files, output_path: output_path)
        expect(File.exist?(output_path)).to be true
        content = File.read(output_path)
        expect(content).to include("# app")
      end
    end
  end

  describe "#generate" do
    it "works with instance as well" do
      instance = described_class.new(module_name, tree_text, files)
      result = instance.generate
      expect(result).to include("# app")
    end
  end
end
